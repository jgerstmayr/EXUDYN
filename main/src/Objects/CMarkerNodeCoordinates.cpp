#include "Main/CSystemData.h"
#include "Autogenerated/CMarkerNodeCoordinates.h"


void CMarkerNodeCoordinates::GetPosition(const CSystemData& cSystemData, Vector3D& position, ConfigurationType configuration) const
{
	if ((Index)cSystemData.GetCNodes()[parameters.nodeNumber]->GetNodeGroup() & (Index)CNodeGroup::ODE2variables)
	{
		position = ((CNodeODE2*)(cSystemData.GetCNodes()[parameters.nodeNumber]))->GetPosition(configuration);
	}
	else { position = Vector3D(0.); } //in other cases, GetPosition might not be implemented
}

void CMarkerNodeCoordinates::ComputeMarkerData(const CSystemData& cSystemData, bool computeJacobian, MarkerData& markerData) const
{
	//A marker 'NodeCoordinate' must be attached to the ODE2 part of a node
	//always markerData.value is written ... also for velocities
	
	Index n = cSystemData.GetCNodes()[parameters.nodeNumber]->GetNumberOfODE2Coordinates(); //works already for both cases
	if (n) //access to GetCurrentCoordinateVector only if not ground node (which has no real coordinates, but it allows to constrain to a ground node 'coordinates' being always zero
	{
		if ((Index)cSystemData.GetCNodes()[parameters.nodeNumber]->GetNodeGroup() & (Index)CNodeGroup::ODE2variables)
		{
			//must be ODE2
			markerData.velocityAvailable = true;
			markerData.vectorValue_t.CopyFrom(( (CNodeODE2*)(cSystemData.GetCNodes()[parameters.nodeNumber]))->GetCurrentCoordinateVector_t());
		}
		else
		{
			markerData.velocityAvailable = false;
		}
		markerData.vectorValue.CopyFrom(((CNodeODE2*)(cSystemData.GetCNodes()[parameters.nodeNumber]))->GetCurrentCoordinateVector());
		//markerData.vectorValue += ((CNodeODE2*)(cSystemData.GetCNodes()[parameters.nodeNumber]))->GetReferenceCoordinateVector(); //leads to memory allocation!
		markerData.vectorValue.MultAdd(1., ((CNodeODE2*)(cSystemData.GetCNodes()[parameters.nodeNumber]))->GetReferenceCoordinateVector()); //no memory allocation

		if (computeJacobian)
		{
			markerData.jacobian.SetScalarMatrix(n,1.); //unit matrix for nodal coordinates
		}
	}
	else //ground node
	{
		markerData.velocityAvailable = true;
		//markerData.value = 0;
		//markerData.value_t = 0; //set this also for ground markers, because it could be asked for in connector!!!!
		markerData.vectorValue.SetNumberOfItems(0);   //indicates that value is zero
		markerData.vectorValue_t.SetNumberOfItems(0); //set this also for ground markers, because it could be asked for in connector!!!!
		markerData.jacobian.SetNumberOfRowsAndColumns(0, 0); //this signals the connector/system not to apply any load or Lagrange multiplier
	}
}

